public class GameEntity {
   private int health;
    public int damage;
    public  String weapon;

    public int getHealth() {
        return health;
    }
    public void setHealth(int health) {
        this.health = health;
    }
    public int getDamage() {
        return damage;
    }
    public void setDamage(int damage) {
        this.damage = damage;
    }
    public void setWeapon() {
        this.weapon = weapon;
    }
}

